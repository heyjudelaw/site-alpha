const buildSourceCountLabel = (value) => (value === 1 ? '1 source' : `${value} sources`);

const buildReportingStats = (sources) => {
  const sourceTierBreakdown = sources.reduce((totals, source) => {
    const tier = source.tier ?? 'Unrated';
    return { ...totals, [tier]: (totals[tier] ?? 0) + 1 };
  }, {});
  const primarySourceCount = sourceTierBreakdown['Tier 1'] ?? 0;
  const secondarySourceCount = sourceTierBreakdown['Tier 2'] ?? 0;

  let verificationLabel = 'Single supporting source';
  if (primarySourceCount >= 2) verificationLabel = 'Multiple primary sources';
  else if (primarySourceCount === 1 && sources.length >= 2) verificationLabel = 'Primary + supporting sources';
  else if (primarySourceCount === 1) verificationLabel = 'Single primary source';
  else if (sources.length >= 2) verificationLabel = 'Supporting-source mix';

  return {
    sourceCount: sources.length,
    sourceCountLabel: buildSourceCountLabel(sources.length),
    primarySourceCount,
    secondarySourceCount,
    sourceTierBreakdown,
    verificationLabel
  };
};

const lowerFirst = (value) => {
  if (!value) return value;
  return value.charAt(0).toLowerCase() + value.slice(1);
};

const formatUtcDate = (value) =>
  new Date(value).toLocaleDateString('en-US', {
    month: 'short',
    day: 'numeric',
    year: 'numeric',
    timeZone: 'UTC'
  });

const buildSourceSummary = (sources) => {
  if (!sources.length) return 'no linked sources';
  const top = sources
    .slice(0, 3)
    .map((source) => source.title)
    .join('; ');
  const extra = sources.length > 3 ? `, plus ${sources.length - 3} additional references` : '';
  return `${top}${extra}`;
};

const buildTierSummary = (sources) => {
  if (!sources.length) return 'no tiered evidence';
  const counts = sources.reduce((totals, source) => {
    const tier = source.tier ?? 'Unrated';
    return { ...totals, [tier]: (totals[tier] ?? 0) + 1 };
  }, {});
  return Object.entries(counts)
    .map(([tier, count]) => `${count} ${tier.toLowerCase()} source${count === 1 ? '' : 's'}`)
    .join(', ');
};

const evidenceBand = (score) => {
  if (score >= 5) return 'high confidence with strong source triangulation';
  if (score === 4) return 'solid confidence with mostly converging evidence';
  if (score === 3) return 'moderate confidence with meaningful open questions';
  if (score === 2) return 'early signal with limited corroboration';
  return 'low confidence and preliminary signal only';
};

const riskBand = (score) => {
  if (score >= 5) return 'material legal or platform-risk exposure';
  if (score === 4) return 'elevated downside if assumptions fail';
  if (score === 3) return 'balanced upside and downside pressure';
  if (score === 2) return 'contained operational risk';
  return 'limited near-term downside';
};

